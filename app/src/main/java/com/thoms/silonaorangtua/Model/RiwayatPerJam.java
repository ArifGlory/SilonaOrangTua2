package com.thoms.silonaorangtua.Model;

public class RiwayatPerJam {
    public String jam;
    public String alamat;

    public RiwayatPerJam(String jam, String alamat) {
        this.jam = jam;
        this.alamat = alamat;
    }

    public String getJam() {
        return jam;
    }

    public void setJam(String jam) {
        this.jam = jam;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }


}
